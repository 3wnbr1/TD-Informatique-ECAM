age=int(input("Quel est ton âge ? "))
if age==6 or age==7:
    print("Tu es un petit poussin.")
elif age==8 or age==9:
    print("Tu es benjamin.")
elif age==10 or age==11:
    print("Tu es minime.")
elif age==12 or age==13:
    print("Tu es cadet.")
else:
    print("Ton âge n'est pas adapté.")